    <center><h2>Jamur Tiram Dan Manfaatnya</h2></center> 

    <p>Selama ini banyak orang mengenal jamur tiram hanya sebagai makanan pelengkap atau jajanan ringan. Padahal, manfaat jamur tiram tidak sebatas itu. Jamur ini menyimpan banyak potensi untuk mendukung kesehatan tubuh.</p>

    <p> Jamur tiram merupakan salah satu jenis jamur yang banyak dijual di pasaran. Jamur ini mengandung banyak zat yang penting bagi tubuh, di antaranya serat, beta glucan, vitamin B, mineral, kalium, dan beberapa jenis karbohidrat. Jamur ini baik dikonsumsi karena bebas lemak, rendah kalori, dan bebas kolesterol.</p>
